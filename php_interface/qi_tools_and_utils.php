<?php
// --------------------------------------------------------------------------------------------------------------
// get_numeric
// --------------------------------------------------------------------------------------------------------------
function get_numeric($val) {
    return is_numeric($val) ? ($val + 0) : 0;
}
// --------------------------------------------------------------------------------------------------------------
// is_numeric_array
// --------------------------------------------------------------------------------------------------------------
function is_numeric_array($str) {
    return preg_match("/^([0-9]+\,)+[0-9]+$/",$str);
}
// --------------------------------------------------------------------------------------------------------------
// get_numeric_array
// --------------------------------------------------------------------------------------------------------------
function get_numeric_array($val) {
    $arr  =explode(',',$val);
    foreach ($arr as &$num) $num = get_numeric($num);
    return $arr;
}
// --------------------------------------------------------------------------------------------------------------
// sys_numbers
// user_numbers
// --------------------------------------------------------------------------------------------------------------
function sys_numbers($var) { return $var <= 2147483647; }
function user_numbers($var) { return $var > 2147483647; }
?>
